/**
 * Extract energetische-sanierung legacy HTML sections from migratedServices.json
 * into src/data/energetischeSanierungLegacyContent.jsx for SeoKnowledgeDrawer.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');

function splitTopLevelBlocks(html) {
  const blocks = [];
  const re = /<(div|section|article)\b[^>]*>/gi;
  let match;
  while ((match = re.exec(html)) !== null) {
    const tag = match[1].toLowerCase();
    const start = match.index;
    const openEnd = html.indexOf('>', start) + 1;
    const tagRe = new RegExp(`</?${tag}\\b[^>]*>`, 'gi');
    tagRe.lastIndex = openEnd;
    let depth = 1;
    let t;
    while ((t = tagRe.exec(html)) !== null) {
      depth += t[0][1] === '/' ? -1 : 1;
      if (depth === 0) {
        blocks.push(html.slice(start, t.index + t[0].length));
        re.lastIndex = t.index + t[0].length;
        break;
      }
    }
  }
  return blocks;
}

function plainText(html) {
  return html.replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim();
}

function extractHeading(html) {
  const h2 = (html.match(/<h2\b[^>]*>([\s\S]*?)<\/h2>/i) || [])[1];
  if (h2) return plainText(h2);
  const h3 = (html.match(/<h3\b[^>]*>([\s\S]*?)<\/h3>/i) || [])[1];
  if (h3) return plainText(h3);
  const label = (html.match(/class="section-label"[^>]*>([\s\S]*?)<\/div>/i) || [])[1];
  return label ? plainText(label) : '';
}

function slugify(s) {
  return s
    .toLowerCase()
    .replace(/ä/g, 'ae')
    .replace(/ö/g, 'oe')
    .replace(/ü/g, 'ue')
    .replace(/ß/g, 'ss')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 80);
}

function escapeForTemplate(s) {
  return s
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');
}

const migrated = JSON.parse(fs.readFileSync(path.join(root, 'src/data/migratedServices.json'), 'utf8'));
const page = migrated.pages['energetische-sanierung-rhein-main'];
if (!page) {
  console.error('No migrated page found for energetische-sanierung-rhein-main');
  process.exit(1);
}

const html = page.contentHtml;
const blocks = splitTopLevelBlocks(html).filter((b) => !/page-hero/i.test(b));

const sections = [];
const seenIds = new Set();

for (const block of blocks) {
  if (/faq-section|faq-list/i.test(block)) continue;
  if (/id="kontakt-formular"|cta-form-wrap/i.test(block)) continue;
  if (/Verwandte Leistungen/i.test(block)) continue;

  const title = extractHeading(block);
  if (!title) continue;

  let id = slugify(title);
  if (seenIds.has(id)) {
    id = `${id}-${sections.length + 1}`;
  }
  seenIds.add(id);

  sections.push({ id, title, html: block });
}

const out = `/* Auto-generated by scripts/build-energetische-sanierung-legacy.mjs – do not edit by hand. */
/* eslint-disable react/no-danger */

export const energetischeSanierungLegacySections = [
${sections
  .map(
    (s) => `  {
    id: '${s.id}',
    title: ${JSON.stringify(s.title)},
    content: (
      <div
        className="br-legacy-migrated-html"
        dangerouslySetInnerHTML={{ __html: \`${escapeForTemplate(s.html)}\` }}
      />
    ),
  }`
  )
  .join(',\n')}
];
`;

fs.writeFileSync(path.join(root, 'src/data/energetischeSanierungLegacyContent.jsx'), out);
console.log(`Wrote ${sections.length} legacy sections.`);
